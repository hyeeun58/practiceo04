package prob01;

public class Printer 
{
	// 숫자(정수) 출력
	public void println(int target)
	{
		System.out.println(target);
	}
	
	// 숫자(실수) 출력
	public void println(double target)
	{
		System.out.println(target);
	}
		
	// 참/거짓 출력
	public void println(boolean target)
	{
		System.out.println(target);
	}
	
	// 문자열 출력
	public void println(String target)
	{
		System.out.println(target);
	}
}
